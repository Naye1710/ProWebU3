	<h2>Unidad 3 I Programacion del lado del Cliente</h2>
	<p class="text-justify">La programación del lado del cliente tiene como principal ventaja que
la ejecución de la aplicación se delega al cliente, con lo cual se evita
recargar al servidor de trabajo. El servidor solo envía el código, y es
tarea del browser interpretarlo.
El navegador es una especie de aplicación capaz de interpretar las
órdenes recibidas en forma de código HTML fundamentalmente y
convertirlas en las páginas que son el resultado de dicha orden</p>

<h3>Definicion</h3>
<p class="text-justify">Es cuando se programa una página web, en la mayoría de los casos se utiliza los que se conocen como “lenguajes del lado del cliente”. Es decir, que el servidor no interviene para nada en el proceso de crear la página web solicitada por el usuario.
Los lenguajes utilizados en este caso son el conocido HTML, Java Script o Visual Basic Script (VBScript), si el navegador a utilizar es Microsoft Internet Explorer. Las páginas creadas con estos lenguajes son enviadas al usuario, de forma que el navegador es el encargado de interpretar y mostrarlas por pantalla. Lógicamente, al ser tecnología de tipo cliente, habrá que prestar especial atención  al navegador utilizado, ya que como ocurre en muchas ocasiones, lo que funciona con uno puede no hacerlo con otro</p>
